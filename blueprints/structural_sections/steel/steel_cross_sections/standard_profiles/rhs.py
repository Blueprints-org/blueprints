    def __init__(self, alias: str, h: MM, b: MM, t: MM, r0: MM, ri: MM) -> None:
        """Initialize RHS profile.
        Args:
            alias (str): Profile alias.
            h (MM): Total height.
            b (MM): Total width.
            t (MM): Thickness.
            r0 (MM): Outer radius.
            ri (MM): Inner radius.
        """
        self.alias = alias
        self.total_height = h
        self.total_width = b
        self.thickness = t
        self.outer_radius = r0
        self.inner_radius = ri
        self.center_radius = r0 - t / 2
