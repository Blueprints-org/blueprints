        """Initialize SHS profile.
        Args:
            alias (str): Profile alias.
            b (MM): Total width.
            t (MM): Thickness.
            r0 (MM): Outer radius.
            ri (MM): Inner radius.
        """
        self.alias = alias
        self.total_width = b
        self.total_height = b
        self.thickness = t
        self.outer_radius = r0
        self.inner_radius = ri
        self.center_radius = r0 - t / 2
